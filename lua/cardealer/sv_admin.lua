hook.Add("Initialize", "MyCarDealer_LoadSavedConfig", function()
    timer.Simple(3, function()
        local saved = file.Read("cardealer/config_vehicles.txt", "DATA")
        if saved then
            print("[Dynora Motor´s] Found saved vehicle config in data/cardealer/config_vehicles.txt")
            print("[Dynora Motor´s] To apply: Copy contents to addons/cardealer/lua/cardealer/config.lua")
        end
    end)
end)

